'''
Ejercicio 1: Operadores Aritméticos

Problema: Escribe un programa que solicite al usuario dos números y 
realice las operaciones de suma, resta, multiplicación, división y módulo.

'''

numero_1=int(input("escribe un numero entero"))
numero_2=int(input("escribe un numero entero"))

print("suma",numero_1 + numero_2)
print("resta",numero_1 - numero_2)
print("multiplicacion",numero_1 * numero_2) 
print("division",numero_1 // numero_2)
print("modulo",numero_1 % numero_2)